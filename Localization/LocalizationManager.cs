        /// <summary>
        /// Gets all translations for a given language.
        /// Returns a copy to prevent external modification of internal state.
        /// </summary>
        public Dictionary<string, string> GetTranslationsForLanguage(AppLanguage language)
        {
            if (_translations.TryGetValue(language, out var translations))
            {
                // Return a COPY to prevent external code from modifying the original
                return new Dictionary<string, string>(translations);
            }
            // Return a copy of English as fallback
            return new Dictionary<string, string>(_translations[AppLanguage.ENGLISH]);
        }

        public string GetString(string key, params object[] args)
        {
            if (_translations.TryGetValue(_currentLanguage, out var dict) && dict.TryGetValue(key, out var value))
            {
                return args.Length > 0 ? string.Format(value, args) : value;
            }

            // Fallback to English
            if (_translations[AppLanguage.ENGLISH].TryGetValue(key, out var fallback))
            {
                return args.Length > 0 ? string.Format(fallback, args) : fallback;
            }

            return key;
        }

        protected void OnPropertyChanged([CallerMemberName] string? name = null)
        {
            PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(name));
        }
    }

