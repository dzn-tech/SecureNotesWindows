            Source = LocalizationManager.Instance;
            Path = new System.Windows.PropertyPath($"Item[{key}]");
            Mode = System.Windows.Data.BindingMode.OneWay;
            UpdateSourceTrigger = System.Windows.Data.UpdateSourceTrigger.PropertyChanged;
        }
